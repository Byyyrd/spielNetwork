public class Arrow {

        double x;
        double y;
        double roration;

        public Arrow(double x, double y, double roration) {
            this.x = x;
            this.y = y;
            this.roration = roration;

        }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getRoration() {
        return roration;
    }
}


